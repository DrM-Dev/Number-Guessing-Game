#_________________________________________________________________SOME VARIABLES/RULES
global SECRET_NUMBER
global ATTEMPTS


#----------------------------------------------difficulty levels
def dif_level(level):
    global ATTEMPTS
    if level == "easy":
        ATTEMPTS = 10
        guessing_number(ATTEMPTS)
    if level == "hard":
        ATTEMPTS = 5
        guessing_number(ATTEMPTS)

#----------------------------------------------Secret Number Generator:
def guessing_number(number_of_attempts):
    global SECRET_NUMBER
    SECRET_NUMBER = random.randint(1, 101)
    #----------------------
    guessing_starts = True
    #----------------------
    player_guess = 0

    while guessing_starts and number_of_attempts > 0:
        print(f"The SECRET number is between 1 and 100, you have {number_of_attempts} attempts to guess it :) ")
        player_guess = int(input("your guess (only numbers): \n "))

        if player_guess == SECRET_NUMBER:
            print(f"YOU GOT IT!! the secret number was {SECRET_NUMBER}!")
            winning_screen() #---------------------------------------------->WIN
        elif player_guess > SECRET_NUMBER:
            print("too high! try lowering your guess v ")
            number_of_attempts -= 1
            print(f"attempts left: {number_of_attempts}")
        elif player_guess < SECRET_NUMBER:
            print("too high! try increasing your guess ^ ")
            number_of_attempts -= 1
            print(f"attempts left: {number_of_attempts}")
        #------------------------------
        elif number_of_attempts <= 0:
            guessing_starts = False
        # ------------------------------
        else:
            print("<!>INVALID INPUT, guess a number please<!>")

    if number_of_attempts <= 0:
        losing_screen() #---------------------------------------------->LOST



#----------------------------------------------MAINCODE
def enter_game():
    logo()
    #--------------------choosing difficulty:
    player_choice = ""
    difficulty_set = False
    difficulty_level = ""

    while not difficulty_set:
        player_choice = input("Chose the game difficulty [Easy] or [Hard]").lower()

        if player_choice == "easy":
            dif_level(player_choice)
            difficulty_set = True
        elif player_choice == "hard":
            dif_level(player_choice)
            difficulty_set = True
        else:
            print("<!>INVALID INPUT, please choose either [Easy] or [Hard]<!> ")


# _________________________________________________________________START THE GAME
enter_game()
